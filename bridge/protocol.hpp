inline uint16_t crc16(const uint8_t * data, uint32_t len)
{
  uint16_t crc = kCrc16Init;
  while (len-- != 0U) {
    const auto byte = *data++;
    const auto index = static_cast<uint8_t>((crc ^ byte) & 0x00FFU);
    crc = static_cast<uint16_t>((crc >> 8U) ^ kCrc16Table[index]);
  }
  return crc;
}

inline void build_referee_frame(
  uint8_t * frame,
  std::size_t & frame_len,
  uint16_t cmd_id,
  uint8_t seq,
  const uint8_t * data,
  std::size_t data_len)
{
  // 协议结构: SOF(1) + data_length(2,LE) + seq(1) + CRC8(1) + cmd_id(2,LE) + data(n) + CRC16(2,LE)
  // data_length 字段含义: 仅 data 段长度, 不包含 cmd_id (协议 1.1 表 1-3).
  // CRC16 范围: 整包校验 = frame_header(5) + cmd_id(2) + data(n) = 7 + n 字节.
  const uint16_t data_length_field = static_cast<uint16_t>(data_len);
  frame[0] = kRefereeSof;
  frame[1] = static_cast<uint8_t>(data_length_field & 0xFFU);
  frame[2] = static_cast<uint8_t>((data_length_field >> 8U) & 0xFFU);
  frame[3] = seq;
  frame[4] = crc8(frame, 4U);

  std::memcpy(&frame[5], &cmd_id, sizeof(cmd_id));
  std::memcpy(&frame[7], data, data_len);

  const std::size_t crc16_offset = 7U + data_len;
  // 整包 CRC16: 覆盖 frame[0..crc16_offset-1], 即 header + cmd_id + data.
  const auto crc16_val = crc16(frame, static_cast<uint32_t>(crc16_offset));
  frame[crc16_offset] = static_cast<uint8_t>(crc16_val & 0xFFU);
  frame[crc16_offset + 1U] = static_cast<uint8_t>((crc16_val >> 8U) & 0xFFU);

  frame_len = crc16_offset + 2U;
}

inline uint16_t checksum16(const uint8_t * data, std::size_t len)
{
  uint16_t sum = 0;
  for (std::size_t i = 0; i < len; ++i) {
    sum = static_cast<uint16_t>(sum + data[i]);
  }
  return sum;
}

inline void fill_custom_client_0310_video_chunk(
  CustomClientVideo0310Chunk & packet,
  uint8_t flags,
  uint8_t sequence,
  const uint8_t * data,
  std::size_t size)
{
  const auto payload_bytes = static_cast<uint16_t>(std::min(size, sizeof(packet.payload)));
  packet.flags_and_payload_bytes_msb = static_cast<uint8_t>(flags & kCustomClientVideo0310FlagReset);
  if ((payload_bytes & 0x0100U) != 0U) {
    packet.flags_and_payload_bytes_msb = static_cast<uint8_t>(
      packet.flags_and_payload_bytes_msb | kCustomClientVideo0310PayloadBytesMsbMask);
  }
  packet.sequence = sequence;
  packet.payload_bytes_lsb = static_cast<uint8_t>(payload_bytes & 0x00FFU);
  if (payload_bytes > 0U) {
    std::memcpy(packet.payload, data, payload_bytes);
  }
}
